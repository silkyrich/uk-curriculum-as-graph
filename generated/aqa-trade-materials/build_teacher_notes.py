"""
build_teacher_notes.py — Build teacher-notes.md from curriculum_unit.json

The teacher notes pane of the PPTX (speaker notes) is also compiled here —
the full section teacher block is written to the first slide of each section.
"""

import json
import sys
from pathlib import Path


def format_misconception(m: dict) -> str:
    source = f" *({m['source']})*" if m.get("source") else ""
    lines = [
        f"- **Error:** {m['error']}",
        f"  **Correction:** {m['correction']}{source}",
    ]
    return "\n".join(lines)


def format_timing(t: dict) -> str:
    parts = [f"{k.replace('_', ' ').title()}: {v} min" for k, v in t.items()]
    total = sum(t.values())
    return f"{' | '.join(parts)} | **Total: {total} min**"


def build_teacher_notes(unit_path: str, output_path: str = None):
    with open(unit_path) as f:
        unit = json.load(f)

    meta = unit.get("meta", {})
    sections = unit.get("sections", [])
    concepts = unit.get("concepts", {})

    lines = []
    lines.append(f"# Teacher Notes: {meta.get('title', 'Unit')}")
    lines.append(f"\n**Subject:** {meta.get('subject', '')}  ")
    lines.append(f"**Unit:** {meta.get('unit', '')}  ")
    lines.append(f"**Spec reference:** {meta.get('spec_ref', '')}  ")
    lines.append(f"**Generated from:** `curriculum_unit.json`\n")
    lines.append("> These notes are generated from the canonical curriculum unit object. "
                 "Edit `curriculum_unit.json` and rebuild — do not edit this file directly.\n")
    lines.append("---\n")

    # Unit overview
    lines.append("## Unit Overview\n")

    total_mins = sum(s.get("estimated_minutes", 0) for s in sections)
    lines.append(f"**Sections:** {len(sections)}  ")
    lines.append(f"**Estimated total time:** {total_mins} minutes ({total_mins // 60}h {total_mins % 60}m)\n")

    # Section index
    lines.append("### Section Index\n")
    lines.append("| # | Section | Time | Concepts introduced |")
    lines.append("|---|---------|------|---------------------|")
    for i, s in enumerate(sections, 1):
        introduced = ", ".join(
            concepts.get(cid, {}).get("name", cid)
            for cid in s.get("concepts_introduced", [])
        )
        lines.append(
            f"| {i} | {s['title']} | {s.get('estimated_minutes', '?')} min | {introduced or '—'} |"
        )
    lines.append("")

    # Spec coverage
    spec_coverage = unit.get("spec_coverage", [])
    if spec_coverage:
        lines.append("### AQA Spec Coverage\n")
        for ref in spec_coverage:
            lines.append(f"- `{ref}`")
        lines.append("")

    lines.append("---\n")

    # Per-section notes
    for i, section in enumerate(sections, 1):
        sid = section["section_id"]
        teacher = section.get("teacher", {})

        lines.append(f"## Section {i}: {section['title']}\n")
        lines.append(f"**Section ID:** `{sid}` | **Est. time:** {section.get('estimated_minutes', '?')} min\n")

        # Spec refs
        spec_refs = section.get("spec_refs", [])
        if spec_refs:
            lines.append(f"**Spec points:** {', '.join(f'`{r}`' for r in spec_refs)}\n")

        # Learning objectives
        objectives = section.get("learning_objectives", [])
        if objectives:
            lines.append("### Learning Objectives\n")
            for obj in objectives:
                ao = obj.get("ao", "")
                lines.append(f"- [{ao}] {obj['text']}")
            lines.append("")

        # Concepts
        introduced = section.get("concepts_introduced", [])
        reinforced = section.get("concepts_reinforced", [])
        if introduced or reinforced:
            lines.append("### Concepts\n")
            if introduced:
                lines.append("**Introduced this section:**")
                for cid in introduced:
                    c = concepts.get(cid, {})
                    defn = c.get("definition", "")
                    lines.append(f"- **{c.get('name', cid)}** (`{cid}`): {defn}")
            if reinforced:
                lines.append("\n**Reinforced from earlier sections:**")
                for cid in reinforced:
                    c = concepts.get(cid, {})
                    lines.append(f"- {c.get('name', cid)} (`{cid}`)")
            lines.append("")

        # Prerequisite knowledge
        prereq = teacher.get("prerequisite_knowledge")
        if prereq:
            lines.append(f"### Prerequisite Knowledge\n\n{prereq}\n")

        # Teaching sequence
        sequence = teacher.get("teaching_sequence")
        if sequence:
            lines.append(f"### Teaching Sequence\n\n{sequence}\n")

        # Timing
        timing = teacher.get("timing")
        if timing:
            lines.append(f"### Timing\n\n{format_timing(timing)}\n")

        # Misconceptions
        misconceptions = teacher.get("misconceptions", [])
        if misconceptions:
            lines.append("### Misconceptions to Pre-empt\n")
            for m in misconceptions:
                lines.append(format_misconception(m))
            lines.append("")

        # Board work
        board_work = teacher.get("board_work")
        if board_work:
            lines.append(f"### Board Work\n\n{board_work}\n")

        # Differentiation
        diff = teacher.get("differentiation", {})
        if diff:
            lines.append("### Differentiation\n")
            if diff.get("stretch"):
                lines.append(f"**Stretch/A*:** {diff['stretch']}")
            if diff.get("support"):
                lines.append(f"\n**Support:** {diff['support']}")
            lines.append("")

        # Additional teaching notes
        extra = teacher.get("notes")
        if extra:
            lines.append(f"### Teaching Notes\n\n{extra}\n")

        # Slides summary (for reference)
        slides = section.get("slides", [])
        if slides:
            lines.append("### Slides in this section\n")
            for sl in slides:
                title = sl.get("title") or sl.get("section_title") or sl.get("type", "")
                lines.append(f"- `{sl.get('slide_id', '?')}` — {sl.get('type', '?')}: {title}")
            lines.append("")

        # Questions summary
        questions = section.get("questions", [])
        if questions:
            lines.append("### Questions in this section\n")
            lines.append("| Q ID | Marks | AOs | Concepts tested |")
            lines.append("|------|-------|-----|-----------------|")
            for q in questions:
                aos = ", ".join(q.get("ao_tags", []))
                tested = ", ".join(
                    concepts.get(cid, {}).get("name", cid)
                    for cid in q.get("concepts_tested", [])
                )
                lines.append(f"| `{q.get('q_id','?')}` | {q.get('marks','?')} | {aos} | {tested or '—'} |")
            lines.append("")

        lines.append("---\n")

    # Resources
    resources = unit.get("resources", [])
    if resources:
        lines.append("## External Resources\n")
        for r in resources:
            free_tag = "(Free)" if r.get("free") else "(Paid/requires login)"
            relevance = ", ".join(r.get("relevance", []))
            lines.append(f"### {r.get('title', r['resource_id'])}\n")
            lines.append(f"- **URL:** {r.get('url', 'TBC')} {free_tag}")
            lines.append(f"- **Type:** {r.get('type', '?')}")
            if relevance:
                lines.append(f"- **Relevant to slides:** {relevance}")
            if r.get("notes"):
                lines.append(f"- **Notes:** {r['notes']}")
            lines.append("")

    output = "\n".join(lines)

    if output_path is None:
        output_path = Path(unit_path).parent / "teacher-notes.md"

    with open(output_path, "w") as f:
        f.write(output)

    print(f"Teacher notes written: {output_path}")
    return str(output_path)


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python3 build_teacher_notes.py <curriculum_unit.json> [output.md]")
        sys.exit(1)
    inp = sys.argv[1]
    out = sys.argv[2] if len(sys.argv) > 2 else None
    build_teacher_notes(inp, out)
